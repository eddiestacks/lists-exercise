import type { NextPage } from 'next'
import Head from 'next/head'
import item from '../components/item'
import sortLists from '../lib/sort'
import useListItems from '../api/useListItems'

const Fetch: NextPage = () => {
  const { data, error } = useListItems('https://fetch-hiring.s3.amazonaws.com/hiring.json')

  if (error) return <div>failed to load</div>
  if (!data) return <div>loading...</div>

  const sortedLists = sortLists(data)

  return (
    <div>
      <Head>
        <title>Fetch Exercise App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <h1>Data</h1>
        <table>
          <thead>
            <tr>
              <th>ID</th>
              <th>List ID</th>
              <th>Name</th>
            </tr>
          </thead>
          <tbody>
            {sortedLists.map((list) => {
              return list.map(item)
            })}
          </tbody>
        </table>
      </main>
    </div>
  )
}

export default Fetch
